package org.team1540.robot2017;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static final int climberTalonTop;
	public static final int climberTalonBottom;
	public static final int feederTalonTop;
	public static final int feederTalonLeft;
	public static final int feederTalonRight;
	public static final int gearSliderTalon;
	public static final int gearServoRightDeploy;
	public static final int gearServoLeftDeploy;
	public static final int intakeTalon;
	public static final int shooterTalonRightFlywheel;
	public static final int shooterTalonLeftFlywheel;
	public static final int shooterTalonBelt = 8;
}
